            <h2>Timelines & Process</h2>
            
            <details>
                <summary>How long does it take to close on a home in Texas?</summary>
                <div class="answer">
                    In Texas, closing typically takes about 30 days from contract execution, though this can vary based on financing type, appraisal timelines, and specific transaction needs. Cash deals can close in as little as 7-14 days, while VA and FHA loans might take 35-45 days.
                </div>
            </details>
            
            <details>
                <summary>What is the option period in Texas?</summary>
                <div class="answer">
                    The option period is a negotiated window (often 5-10 days) during which the buyer can terminate the contract for any reason. The buyer typically pays a non-refundable option fee (usually $100-500) directly to the seller for this right. During this time, inspections are conducted and repairs negotiated.
                </div>
            </details>
            
            <details>
                <summary>Should I get pre-approved before looking at homes?</summary>
                <div class="answer">
                    Yes, getting pre-approved before showings is strongly recommended. In DFW's competitive market, sellers often won't consider offers without pre-approval. It shows you're serious, speeds up the process, and helps you know your exact budget. I can refer you to trusted lenders who specialize in different loan types.
                </div>
            </details>
