        <section id="pre-approval">
            <h2>Pre-Approval Pack</h2>
            <p>In DFW's competitive market, showing up without pre-approval is like bringing a knife to a gunfight. Here's my pre-approval pack that gets buyers ready:</p>
            
            <p><strong>The Documents (digital folder, ready to go):</strong></p>
            <ul>
                <li>Last 2 years tax returns</li>
                <li>Last 2 months bank statements (all pages)</li>
                <li>Last 30 days pay stubs</li>
                <li>Driver's license copy</li>
                <li>Employment verification letter (if needed)</li>
            </ul>
            
            <p><strong>The Lenders (my trusted DFW network):</strong></p>
            <ul>
                <li>Conventional: 2-3 options, competitive rates</li>
                <li>FHA/VA: specialists who know the programs</li>
                <li>Jumbo: for properties over $766,550</li>
                <li>Hard money: for flips and quick closes</li>
            </ul>
            
            <p><strong>The Timeline:</strong></p>
            <ul>
                <li>Day 1: Submit to lender</li>
                <li>Day 2-3: Initial review and conditions</li>
                <li>Day 4-5: Provide additional docs if needed</li>
                <li>Day 5-7: Pre-approval letter in hand</li>
            </ul>
            
            <p>Pro tip: Get pre-approved for 10% more than your target. Gives negotiation room without re-running credit.</p>
        </section>
        
        <section id="offer-playbook">
            <h2>Offer Playbook</h2>
            <p>Winning offers in DFW isn't just about price. It's about terms, timing, and presentation.</p>
            
            <p><strong>My offer template includes:</strong></p>
            <ul>
                <li><strong>Cover letter:</strong> Personal touch, why this house, quick close ability</li>
                <li><strong>Proof of funds:</strong> Bank statement or pre-approval attached</li>
                <li><strong>Escalation clause:</strong> Auto-increase to beat competitors by $X</li>
                <li><strong>Option period:</strong> 5-7 days standard, can go lower for strength</li>
                <li><strong>Earnest money:</strong> 1% minimum, 2-3% shows serious intent</li>
                <li><strong>Closing date:</strong> Flexible to seller's needs</li>
            </ul>
            
            <p><strong>Multiple offer strategy:</strong></p>
            <ul>
                <li>First offer: Strong but leave room</li>
                <li>Best and final: Pull all levers</li>
                <li>Backup position: Stay engaged if you lose</li>
            </ul>
            
            <p><strong>Recent win:</strong> Richardson property, 11 offers. Won with 3rd highest price by offering 21-day close and waiving repair requests under $5K.</p>
        </section>
        
        <section id="tc-pipeline">
            <h2>TC Pipeline (contract-to-close)</h2>
            <p>This is where deals die or thrive. My TC pipeline keeps everything moving:</p>
            
            <p><strong>Week 1 - Contract Execution:</strong></p>
            <ul>
                <li>Fully executed contract to all parties</li>
                <li>Earnest money delivered (next business day)</li>
                <li>Option fee delivered (within 3 days)</li>
                <li>Title company opened</li>
                <li>Lender notified and contract sent</li>
            </ul>
            
            <p><strong>Week 2 - Inspections:</strong></p>
            <ul>
                <li>General inspection scheduled</li>
                <li>Specialized inspections if needed (pool, foundation, roof)</li>
                <li>Repair amendment negotiated</li>
                <li>Option period exit decision</li>
            </ul>
            
            <p><strong>Week 3-4 - Appraisal & Underwriting:</strong></p>
            <ul>
                <li>Appraisal ordered and scheduled</li>
                <li>Loan conditions gathered</li>
                <li>Insurance quotes obtained</li>
                <li>HOA docs reviewed</li>
                <li>Survey reviewed (or ordered)</li>
            </ul>
            
            <p><strong>Final Week - Closing Prep:</strong></p>
            <ul>
                <li>Final walkthrough scheduled</li>
                <li>Utilities transfer coordinated</li>
                <li>Closing disclosure reviewed</li>
                <li>Wire instructions verified (call to confirm!)</li>
                <li>Keys, garage openers, gate codes transferred</li>
            </ul>
            
            <p>Every step tracked in my system with automated reminders. Nothing falls through cracks.</p>
        </section>
        
        <section id="inspection-close">
            <h2>Inspection → Close</h2>
            <p>The option period is your due diligence window. Use it wisely:</p>
            
            <p><strong>Inspection strategy:</strong></p>
            <ul>
                <li>Day 1-2: General inspection</li>
                <li>Day 3-4: Review report, get contractor quotes</li>
                <li>Day 5: Submit repair amendment</li>
                <li>Day 6-7: Negotiate and decide</li>
            </ul>
            
            <p><strong>What to ask for:</strong></p>
            <ul>
                <li>Safety issues: Always (electrical, gas, structural)</li>
                <li>Major mechanicals: HVAC over 15 years, roof issues</li>
                <li>Water damage: Active leaks, past damage</li>
                <li>Foundation: Any movement or drainage issues</li>
            </ul>
            
            <p><strong>What to let go:</strong></p>
            <ul>
                <li>Cosmetic issues</li>
                <li>Normal wear and tear</li>
                <li>Items under $500 (unless multiple)</li>
                <li>Anything disclosed upfront</li>
            </ul>
            
            <p><strong>Closing week checklist:</strong></p>
            <ul>
                <li>Monday: Final loan approval</li>
                <li>Tuesday: Review closing disclosure</li>
                <li>Wednesday: Final walkthrough</li>
                <li>Thursday: Wire funds (morning only)</li>
                <li>Friday: Sign and get keys</li>
            </ul>
        </section>
        
